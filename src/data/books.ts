export function getAllBooks(): Book[] {
  return books;
}

export function getBookById(id: string): Book | undefined {
  return books.find((b) => b.id === id);
}

export function getBookCategories(): string[] {
  const cats: string[] = [];
  books.forEach((b) => {
    if (!cats.includes(b.category)) {
      cats.push(b.category);
    }
  });
  return cats;
}
